package com.company.vechicles;

public class Vechicle {
    String producer;
    String model;
    Double value;
    String color;
    Double millage;


    public Vechicle(String producer, String model, Double value, String color, Double millage) {
        this.producer = producer;
        this.model = model;
        this.value = value;
        this.color = color;
        this.millage = millage;
    }
}
