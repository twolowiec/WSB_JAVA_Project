package com.company.vechicles;

public class Vechicle {
    String producer;
    String model;
    Double value;
    String color;
    Double millage;
    Boolean needRepairs;


    public Vechicle(String producer, String model, Double value, String color, Double millage, Boolean needRepairs) {
        this.producer = producer;
        this.model = model;
        this.value = value;
        this.color = color;
        this.millage = millage;
        this.needRepairs = needRepairs;
    }


}
